package wuxiaworld.helper;

public class Novels {

	private int total;
	private boolean result;
	private Novel[] items;

	public int getTotal() {
		return total;
	}

	public void setTotal(int total) {
		this.total = total;
	}

	public boolean isResult() {
		return result;
	}

	public void setResult(boolean result) {
		this.result = result;
	}

	public Novel[] getItems() {
		return items;
	}

	public void setItems(Novel[] items) {
		this.items = items;
	}
}
